/**
 * 住所文字列から全粒度のコードを解決。
 * 対応外（地方の市町村など）は null。
 */
export function resolveArea(address: string): ResolvedArea | null {
  // 都道府縣 を先頭で判定
  const prefMatch = address.match(/^(北海道|.+?[都府県])/)
  if (!prefMatch) return null
  const prefName = prefMatch[1]
  const prefCode2 = PREF_CODE[prefName]
  if (!prefCode2) return null

  const rest = address.slice(prefName.length)

  // パターン A: 政令市 + 区（例: "横浜市南区"）
  const deepMatch = rest.match(/^(.+?市)(.+?区)/)
  if (deepMatch) {
    const fullKey = prefName + deepMatch[1] + deepMatch[2]
    const cityCode5 = CITY_CODE_MAP[fullKey]
    if (cityCode5) {
      const majorCityCode5 = CITY_TO_MAJOR[cityCode5] ?? cityCode5
      return {
        full: prefName + deepMatch[1] + deepMatch[2],
        prefCode2, prefName,
        cityCode5,
        cityName: deepMatch[2],
        majorCityCode5,
        majorCityName: MAJOR_CITY_NAME[majorCityCode5] ?? deepMatch[1],
      }
    }
  }

  // パターン B: 東京都特別区（例: "目黒区"）
  if (prefName === '東京都') {
    const wardMatch = rest.match(/^(.+?区)/)
    if (wardMatch) {
      const fullKey = prefName + wardMatch[1]
      const cityCode5 = CITY_CODE_MAP[fullKey]
      if (cityCode5) {
        return {
          full: fullKey,
          prefCode2, prefName,
          cityCode5,
          cityName: wardMatch[1],
          majorCityCode5: '13100',
          majorCityName: '東京都特別区部',
        }
      }
    }
  }

  // パターン C: 一般市町村（例: "武蔵野市"）
  const cityMatch = rest.match(/^(.+?[市町村])/)
  if (cityMatch) {
    const fullKey = prefName + cityMatch[1]
    const cityCode5 = CITY_CODE_MAP[fullKey]
    if (cityCode5) {
      return {
        full: fullKey,
        prefCode2, prefName,
        cityCode5,
        cityName: cityMatch[1],
        majorCityCode5: cityCode5,
        majorCityName: cityMatch[1],
      }
    }
  }

  return null
}
