/**
 * 住所 → 複数粒度の行政区コードへの逆引き
 * =======================================
 * e-Stat の各統計は粒度が異なる（町丁目/市区町村/21大都市/都道府県）ため、
 * 1 回の住所解析で全粒度のコードを返し、各統計モジュールは必要な粒度を選ぶ。
 *
 * 粒度の階層（細 → 粗）:
 *   1. cityCode5      5桁 JIS X 0402（例：13103 港区、14105 横浜市南区）
 *   2. majorCityCode5 5桁「21大都市」レベル（例：13100 東京都特別区部、14100 横浜市、14130 川崎市）
 *   3. prefCode2      2桁 都道府県コード（例：13 東京都）
 *
 * 対応地域は主要大都市圏（東京23区 + 多摩7市 + 政令指定都市9つ = 合計 ~170 地域）。
 * 地方の市町村は resolveArea() が null を返し、各統計モジュールは null なら null を返す。
 */

